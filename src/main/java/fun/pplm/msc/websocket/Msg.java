package fun.pplm.msc.websocket;

public class Msg {
	private String id;
	private String img;
	
	public Msg() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}
	
}
